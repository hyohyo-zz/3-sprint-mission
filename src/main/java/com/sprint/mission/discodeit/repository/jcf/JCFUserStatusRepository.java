package com.sprint.mission.discodeit.repository.jcf;

import com.sprint.mission.discodeit.entity.UserStatus;
import com.sprint.mission.discodeit.repository.UserStatusRepository;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.Objects;
import java.util.Optional;
import java.util.UUID;
import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.stereotype.Repository;

@ConditionalOnProperty(name = "discodeit.repository.type", havingValue = "jcf", matchIfMissing = true)
@Repository
public class JCFUserStatusRepository implements UserStatusRepository {

  private final Map<UUID, UserStatus> data;

  public JCFUserStatusRepository() {
    this.data = new HashMap<>();
  }

  @Override
  public UserStatus create(UserStatus userStatus) {
    this.data.put(userStatus.getUserId(), userStatus);
    return userStatus;
  }

  @Override
  public Optional<UserStatus> find(UUID id) {
    return Optional.ofNullable(this.data.get(id));
  }

  @Override
  public List<UserStatus> findAll() {
    return this.data.values().stream().toList();
  }

  @Override
  public Optional<UserStatus> findByUserId(UUID userId) {
    return data.values().stream()
        .filter(userStatus -> Objects.equals(userStatus.getUserId(), userId))
        .findFirst();
  }

  @Override
  public boolean existsById(UUID id) {
    return this.data.containsKey(id);
  }

  @Override
  public void deleteById(UUID id) {
    this.data.remove(id);
  }

  @Override
  public void deleteByUserId(UUID userId) {
    this.findByUserId(userId)
        .ifPresent(userStatus -> this.deleteByUserId(userStatus.getId()));
  }
}
